void UAuraAbilitySystemLibrary::GetLivePlayersWithInRadius(const UObject* WorldContextObject, TArray<AActor*>& OutOverlappingActors, const TArray<AActor*>& ActorsToIgnore, float Radius, const FVector& SphereOrigin)
{
	FCollisionQueryParams SphereParams;

	SphereParams.AddIgnoredActors(ActorsToIgnore);

	TArray<FOverlapResult> Overlaps;
	if (UWorld* World = GEngine->GetWorldFromContextObject(WorldContextObject, EGetWorldErrorMode::LogAndReturnNull))
	{
		World->OverlapMultiByObjectType(Overlaps, SphereOrigin, FQuat::Identity, FCollisionObjectQueryParams(FCollisionObjectQueryParams::InitType::AllDynamicObjects), FCollisionShape::MakeSphere(Radius), SphereParams);
		for (FOverlapResult& Overlap : Overlaps)
		{
			if (Overlap.GetActor()->Implements<UCombatInterface>() && !ICombatInterface::Execute_IsDead(Overlap.GetActor()) && !Overlap.GetActor()->Implements<UPlayerInterface>())
			{
				OutOverlappingActors.AddUnique(ICombatInterface::Execute_GetAvatar(Overlap.GetActor()));
			}
		}
	}
}

void UAuraAbilitySystemLibrary::GetClosestTargets(int32 MaxNumOfTargets, const TArray<AActor*>& Actors, TArray<AActor*>& OutClosestTargets, const FVector& Origin)
{
	if (MaxNumOfTargets >= Actors.Num())
	{
		OutClosestTargets = Actors;
		return;
	}
	else
	{
		TArray<AActor*> ActorsToCheck = Actors;
		
		int32 NumOfAddtionalTargets = 0;
		

		while (NumOfAddtionalTargets < MaxNumOfTargets)
		{
			if (ActorsToCheck.Num() == 0) break;
			
			double ClosestDist = TNumericLimits<double>::Max();
			AActor* ClosestActor;
			for (AActor* Actor : ActorsToCheck)
			{
				if (Actor->ActorHasTag(FName("Player")) || !Actor->HasAuthority()) continue;

				const double Dist = (Actor->GetActorLocation() - Origin).Length();
				if (ClosestDist > Dist)
				{
					ClosestDist = Dist;
					ClosestActor = Actor;
				}
			}

			ActorsToCheck.Remove(ClosestActor);
			OutClosestTargets.AddUnique(ClosestActor);
			++NumOfAddtionalTargets;
		}
	}
}

bool UAuraAbilitySystemLibrary::IsFriend(AActor* FirstActor, AActor* SecondActor)
{
	// 둘다 Player거나 Enemy라면 true이므로 서로 공격못하게 설정, false라면 서로 다르므로 서로 공격가능하게 설정. 
	return FirstActor->ActorHasTag(FName("Player")) == SecondActor->ActorHasTag(FName("Player"));
}

FGameplayEffectContextHandle UAuraAbilitySystemLibrary::ApplyDamageEffect(const FDamageEffectParams& DamageEffectParams)
{
	const AActor* SourceAvatarActor = DamageEffectParams.SourceAbilitySystemComponent->GetAvatarActor();

	FGameplayEffectContextHandle EffectContextHandle = DamageEffectParams.SourceAbilitySystemComponent->MakeEffectContext();
	EffectContextHandle.AddSourceObject(SourceAvatarActor);
	
	SetDeathImpulse(EffectContextHandle, DamageEffectParams.DeathImpulse);
	SetKnockbackForce(EffectContextHandle, DamageEffectParams.KnockbackForce);
	SetIsRadialDamage(EffectContextHandle, DamageEffectParams.bIsRadialDamage);
	SetRadialDamageInnerRadius(EffectContextHandle, DamageEffectParams.RadialDamageInnerRadius);
	SetRadialDamageOuterRadius(EffectContextHandle, DamageEffectParams.RadialDamageOuterRadius);
	SetRadialDamageOrigin(EffectContextHandle, DamageEffectParams.RadialDamageOrigin);

	FGameplayEffectSpecHandle SpecHandle = DamageEffectParams.SourceAbilitySystemComponent->MakeOutgoingSpec(
		DamageEffectParams.DamageGameplayEffectClass, 
		DamageEffectParams.AbilityLevel,
		EffectContextHandle);

	UAbilitySystemBlueprintLibrary::AssignTagSetByCallerMagnitude(SpecHandle, DamageEffectParams.DamageType, DamageEffectParams.BasedDamage);
	UAbilitySystemBlueprintLibrary::AssignTagSetByCallerMagnitude(SpecHandle, FAuraGameplayTags::Get().Debuff_Chance, DamageEffectParams.DebuffChance);
	UAbilitySystemBlueprintLibrary::AssignTagSetByCallerMagnitude(SpecHandle, FAuraGameplayTags::Get().Debuff_Damage, DamageEffectParams.DebuffDamage);
	UAbilitySystemBlueprintLibrary::AssignTagSetByCallerMagnitude(SpecHandle, FAuraGameplayTags::Get().Debuff_Duration, DamageEffectParams.DebuffDuration);
	UAbilitySystemBlueprintLibrary::AssignTagSetByCallerMagnitude(SpecHandle, FAuraGameplayTags::Get().Debuff_Frequency, DamageEffectParams.DebuffFrequency);
	
	DamageEffectParams.TargetAbilitySystemComponent->ApplyGameplayEffectSpecToSelf(*SpecHandle.Data);

	return EffectContextHandle;
}

TArray<FVector> UAuraAbilitySystemLibrary::EvenlyRotateVectors(const FVector& Forward, const FVector& Axis, float Spread, int32 NumOfVectors)
{
	TArray<FVector> Vectors;

	const FVector LeftOfSpread = Forward.RotateAngleAxis(-Spread / 2.f, FVector::UpVector);

	/*NumOfProjectiles = FMath::Min(GetAbilityLevel(), MaxNumOfProjectiles);*/

	if (NumOfVectors > 1)
	{
		// LeftOfSpeard에서부터 시작해서 오른쪽으로 회전하므로 -1을 해줘야함. Spread 90도에 발사체 2개라면 LeftOfSpread에 1개, 90도 회전해서 1개. Ex) 90 / (2 - 1)
		const float DeltaSpread = Spread / (NumOfVectors - 1);
		for (int32 i = 0; i < NumOfVectors; i++)
		{
			const FVector Direction = LeftOfSpread.RotateAngleAxis(DeltaSpread * i, FVector::UpVector);
			Vectors.Add(Direction);
		}
	}
	else
	{
		Vectors.Add(Forward);
	}

	return Vectors;
}

TArray<FRotator> UAuraAbilitySystemLibrary::EvenlySpacedRotators(const FVector& Forward, const FVector& Axis, float Spread, int32 NumOfRotators)
{
	TArray<FRotator> Rotators;

	const FVector LeftOfSpread = Forward.RotateAngleAxis(-Spread / 2.f, FVector::UpVector);

	/*NumOfProjectiles = FMath::Min(GetAbilityLevel(), MaxNumOfProjectiles);*/

	if (NumOfRotators > 1)
	{
		// LeftOfSpeard에서부터 시작해서 오른쪽으로 회전하므로 -1을 해줘야함. Spread 90도에 발사체 2개라면 LeftOfSpread에 1개, 90도 회전해서 1개. Ex) 90 / (2 - 1)
		const float DeltaSpread = Spread / (NumOfRotators - 1);
		for (int32 i = 0; i < NumOfRotators; i++)
		{
			const FVector Direction = LeftOfSpread.RotateAngleAxis(DeltaSpread * i, FVector::UpVector);
			Rotators.Add(Direction.Rotation());
		}
	}
	else
	{
		Rotators.Add(Forward.Rotation());
	}

	return Rotators;
}

